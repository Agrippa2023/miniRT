#include "libft.h"

void	*ft_free(void ***ptr)
{
	if (*ptr && **ptr)
	{
		free(**ptr);
		**ptr = NULL;
	}
	if (*ptr)
	{
		free(*ptr);
		*ptr = NULL;
	}
	return (NULL);
}
